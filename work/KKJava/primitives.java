package work.KKJava;

public class primitives {
    // Primitive: any data type that cannot be broken down further

    public static void main(String args[]) {

        // primitve
        int n = 20; // size:4 bytes

        char l = 'r';

        float f = 25.01f;
        // size: 4 bytes

        double d = 100.02;
        // double: for large decimal numbers - 8 bytes
        // * DOUBLE is the biggest one

        long ln = 4512367124578936L;
        // long: for large integer values

        // @NOTE:
        // usage of 'f,L' at the end of ln,f ... to depict that they belong to
        // respective data-type

        // -------------------
        String st = "appu"; // String is not primitve

        // $ No typeof or sizeof operator in JAVA
    }
}
